public class TmaxSpeaker {
   public TmaxSpeaker() {
      System.out.println("===> TMaxSpeaker 객체생성");


   }

   public void volumeUp() {
      System.out.println("TmaxSpeaker --- 소리 울린다");
      
   }

   public void volumeDown() {
      System.out.println("TmaxSpeaker --- 소리 낮춘다");
      
   }
}